#include "blake2b.h"
#include <stdio.h>
#include <stdint.h>
#include <string.h>

int main() {
    uint8_t header[32] = {
        0x78,0xfd,0xe5,0x79,0x52,0xe5,0xfc,0x03,0x8c,0x87,0x70,0x4e,0x24,0xc4,0xe0,0x30,
        0x2a,0xe5,0x12,0x24,0x97,0x7a,0x6a,0x90,0x3e,0x94,0x30,0xec,0xae,0x48,0x04,0x93
    };
    uint8_t nonce_le[8] = {0xe0,0x17,0x00,0x00,0x00,0x00,0x00,0x00};
    uint8_t data[40];
    memcpy(data, header, 32);
    memcpy(data+32, nonce_le, 8);

    uint8_t hash[32];
    blake2b(hash, 32, data, 40, NULL, 0);
    for (int i = 0; i < 32; ++i)
        printf("%02x", hash[i]);
    printf("\n");
    return 0;
}
